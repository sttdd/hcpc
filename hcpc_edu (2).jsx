function Quiz({ quiz, color, onDone, dark=false }) {
  const [i, setI] = useState(0);
  const [sel, setSel] = useState(null);
  const [score, setScore] = useState(0);
  const [finished, setFinished] = useState(false);
  const [streak, setStreak] = useState(0);
  const q = quiz[i];

  function pick(idx) {
    if (sel !== null) return;
    setSel(idx);
    if (idx === q.ok) { setScore(s=>s+1); setStreak(s=>s+1); } else setStreak(0);
  }

  function next() {
    if (i+1 >= quiz.length) { setFinished(true); onDone?.(score+(sel===q.ok?1:0)); }
    else { setI(n=>n+1); setSel(null); }
  }

  if (finished) {
    const final = score;
    const pct = Math.round(final/quiz.length*100);
    const msg = pct===100 ? ["🏆","Идеально! Ты всё усвоил!","#22c55e"]
               : pct>=66 ? ["👍","Хорошо! Почти всё правильно.","#f59e0b"]
               : ["📖","Стоит перечитать урок.","#ef4444"];
    return (
      <div style={{textAlign:"center",padding:"20px 0"}}>
        <div style={{fontSize:64,marginBottom:12}}>{msg[0]}</div>
        <div style={{color:msg[2],fontWeight:900,fontSize:28,marginBottom:6}}>{final}/{quiz.length}</div>
        <StarRating score={final} total={quiz.length}/>
        <div style={{color: dark?"#94a3b8":"#64748b",fontSize:15,marginTop:10,marginBottom:32}}>{msg[1]}</div>
        <div style={{display:"flex",gap:10,justifyContent:"center"}}>
          {quiz.map((_,idx)=>(
            <div key={idx} style={{width:40,height:40,borderRadius:"50%",
              background:idx<final?(dark?"#14532d":"#dcfce7"):(dark?"#450a0a":"#fee2e2"),
              border:`2px solid ${idx<final?"#22c55e":"#ef4444"}`,
              display:"flex",alignItems:"center",justifyContent:"center",fontSize:18,
              color:idx<final?"#22c55e":"#ef4444"}}>
              {idx<final?"✓":"✗"}
            </div>
          ))}
        </div>
      </div>
    );
  }

  return (
    <div>
      <div style={{display:"flex",justifyContent:"space-between",alignItems:"center",marginBottom:8}}>
        <span style={{color:"#94a3b8",fontSize:13}}>Вопрос {i+1} из {quiz.length}</span>
        {streak>1&&<span style={{color:"#f59e0b",fontWeight:700,fontSize:13}}>🔥 {streak} подряд!</span>}
      </div>
      <div style={{color: dark?"#f1f5f9":"#1e293b",fontSize:17,fontWeight:700,marginBottom:24,lineHeight:1.5}}>{q.q}</div>
      <div style={{display:"flex",flexDirection:"column",gap:10}}>
        {q.a.map((opt,idx)=>{
          let bg = dark?"#1e293b":"#f8fafc";
          let border = dark?"#334155":"#e2e8f0";
          let tc = dark?"#cbd5e1":"#374151";
          let icon="";
          if (sel!==null) {
            if (idx===q.ok) { bg=dark?"#14532d":"#dcfce7"; border="#22c55e"; tc=dark?"#86efac":"#15803d"; icon="✓ "; }
            else if (idx===sel) { bg=dark?"#450a0a":"#fee2e2"; border="#ef4444"; tc=dark?"#fca5a5":"#dc2626"; icon="✗ "; }
          }
          return (
            <button key={idx} onClick={()=>pick(idx)} style={{
              background:bg, border:`2px solid ${border}`,
              borderRadius:14, padding:"13px 18px",
              cursor:sel!==null?"default":"pointer",
              color:tc, fontSize:15, textAlign:"left",
              fontFamily:"inherit", transition:"all .2s",
              display:"flex",alignItems:"center",gap:8
            }}>
              <span style={{
                minWidth:28,height:28,borderRadius:8,
                background:sel===null?color+"20":idx===q.ok?"#22c55e20":idx===sel?"#ef444420":"transparent",
                border:`1.5px solid ${sel===null?color+"60":idx===q.ok?"#22c55e":idx===sel?"#ef4444":"transparent"}`,
                display:"flex",alignItems:"center",justifyContent:"center",
                color:sel===null?color:idx===q.ok?"#22c55e":idx===sel?"#ef4444":"#94a3b8",
                fontSize:11,fontWeight:900,flexShrink:0
              }}>
                {icon || ["А","Б","В","Г"][idx]}
              </span>
              {opt}
            </button>
          );
        })}
      </div>
      {sel!==null&&(
        <button onClick={next} style={{
          marginTop:20,width:"100%",
          background:`linear-gradient(135deg,${color},${color}cc)`,
          border:"none",borderRadius:14,color:"white",
          fontSize:15,fontWeight:700,padding:"14px",cursor:"pointer",
          boxShadow:`0 4px 16px ${color}44`
        }}>
          {i+1>=quiz.length?"Завершить →":"Следующий →"}
        </button>
      )}
    </div>
  );
}

