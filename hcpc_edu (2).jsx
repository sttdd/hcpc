function StarRating({ score, total }) {
  const pct = score / total;
  const stars = pct >= 1 ? 3 : pct >= 0.67 ? 2 : 1;
  return (
    <div style={{fontSize:28}}>
      {[1,2,3].map(i=>(
        <span key={i} style={{opacity:i<=stars?1:0.2}}>⭐</span>
      ))}
    </div>
  );
}

