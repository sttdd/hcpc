// Совместимость: проверяем проблемы в сборке
function checkCompat(build) {
  const issues = [];
  const hints  = [];
  const { cpu, mobo, ram, psu, cooling } = build;

  if (cpu && cooling) {
    if (cooling.maxTdp < cpu.tdp)
      issues.push(`❄️ Кулер не справится: его максимум ${cooling.maxTdp}Вт, а CPU требует ${cpu.tdp}Вт`);
    else if (cooling.maxTdp >= cpu.tdp * 1.5)
      hints.push("✅ Охлаждение отличное — процессор не перегреется");
  }
  if (cpu && psu) {
    const needed = cpu.tdp + (build.gpu?.vram ? 150 : 20) + 60;
    if (psu.watts < needed)
      issues.push(`🔌 БП слабоват: нужно ~${needed}Вт, у тебя ${psu.watts}Вт`);
  }
  if (ram && ram.size < 8)
    issues.push("⚡ 8 ГБ RAM — минимум для комфортной работы");
  if (build.gpu && build.gpu.id === "g0" && build.storage)
    hints.push("💡 Встроенная графика — отлично для учёбы, но не для игр");
  if (build.storage && build.storage.type === "HDD")
    hints.push("💡 HDD медленный — Windows будет грузиться долго. Рекомендуем SSD");
  return { issues, hints };
}

// Считаем общий балл производительности
function buildScore(build, scenario) {
  const weights = { cpu:0.3, gpu:0.3, ram:0.2, storage:0.2 };
  let score = 0, w = 0;
  for (const [key, weight] of Object.entries(weights)) {
    if (build[key]?.perf?.[scenario]) { score += build[key].perf[scenario] * weight; w += weight; }
  }
  return w > 0 ? Math.round(score / w) : 0;
}

